public class Calculadora{
    public void soma(int numero1, int numero2){
        int resultado = numero1 + numero2;
        System.out.println("O resultado da soma foi de: "+resultado);
    }
    public void soma(int numero1, int numero2, int numero3){
        int resultado = numero1 + numero2 + numero3;
        System.out.println("O resultado da soma foi de: "+resultado);
    }
    public void soma(int numero1, int numero2, int numero3, int numero4){
        int resultado = numero1 + numero2 + numero3 + numero4;
        System.out.println("O resultado da soma foi de: "+resultado);
    } 
}